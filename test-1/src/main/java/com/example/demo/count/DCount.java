package com.example.demo.count;

public class DCount {
	
	
	int tid;
	String tname;
	String price;
	
	
	
	
	
	
	public DCount() {
		super();
	}






	public DCount(int tid, String tname, String price) {
		super();
		this.tid = tid;
		this.tname = tname;
		this.price = price;
	}






	public int getTid() {
		return tid;
	}






	public void setTid(int tid) {
		this.tid = tid;
	}






	public String getTname() {
		return tname;
	}






	public void setTname(String tname) {
		this.tname = tname;
	}






	public String getPrice() {
		return price;
	}






	public void setPrice(String price) {
		this.price = price;
	}
	
	
	
	
	
	
	
	
	

}
