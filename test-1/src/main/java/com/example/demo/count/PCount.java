package com.example.demo.count;

public class PCount {
	int id;
	String name;
    String fname;
	String phone;
	String age;
	String gender;
	String blood;
	String address;
	String addoc;
	String unit;
	String admitdate;
	String seat;
	public PCount() {
		super();
	}
	public PCount(int id, String name, String fname, String phone, String age, String gender, String blood,
			String address, String addoc, String unit, String admitdate, String seat) {
		super();
		this.id = id;
		this.name = name;
		this.fname = fname;
		this.phone = phone;
		this.age = age;
		this.gender = gender;
		this.blood = blood;
		this.address = address;
		this.addoc = addoc;
		this.unit = unit;
		this.admitdate = admitdate;
		this.seat = seat;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBlood() {
		return blood;
	}
	public void setBlood(String blood) {
		this.blood = blood;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAddoc() {
		return addoc;
	}
	public void setAddoc(String addoc) {
		this.addoc = addoc;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getAdmitdate() {
		return admitdate;
	}
	public void setAdmitdate(String admitdate) {
		this.admitdate = admitdate;
	}
	public String getSeat() {
		return seat;
	}
	public void setSeat(String seat) {
		this.seat = seat;
	}
	
	
	
	
	

}
